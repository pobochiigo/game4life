func main() {
	errChan := make(chan error)
	stopChan := make(chan os.Signal, 1)
	signal.Notify(stopChan, syscall.SIGINT, syscall.SIGTERM)

	ctx, cancel := context.WithTimeoutCause(context.Background(), 60*time.Second, errors.New("time's up"))
	defer func() {
		close(errChan)
		close(stopChan)
		cancel()
	}()

	r1 := rule.Underpopulation(nil)
	r2 := rule.Reproduction(r1)
	r3 := rule.Overcrowding(r2)
	rules := rule.Normal(r3)

	g := game.New(rules)

	g.Init(seed)

	g.PrintPopulation()

	go func() {
		t := time.NewTicker(time.Second)

		for {
			select {
			case <-t.C:
				g.Tick()
				g.PrintPopulation()
			case sig := <-stopChan:
				errChan <- fmt.Errorf("game stopped by %v", sig)
				return
			case <-ctx.Done():
				errChan <- fmt.Errorf("game stopped by %w", context.Cause(ctx))
				return
			}
		}
	}()

	if err := <-errChan; err != nil {
		fmt.Println(err)
	}

	fmt.Println("game of life is over")
}
